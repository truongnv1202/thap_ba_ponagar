type MessageKey = keyof typeof messages.vi;

const LANGS: Language[] = ["vi", "en", "zh", "ko"];

function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  let out = template;
  for (const [k, v] of Object.entries(vars)) {
    out = out.split(`{${k}}`).join(String(v));
  }
  return out;
}

const LanguageContext = createContext<{
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: MessageKey, vars?: Record<string, string | number>) => string;
} | null>(null);

function readStoredLanguage(): Language {
  const saved = localStorage.getItem("pg_lang");
  if (saved === "en" || saved === "zh" || saved === "vi" || saved === "ko") return saved;
  return "vi";
}

export function LanguageProvider({ children }: PropsWithChildren) {
  const [language, setLanguageState] = useState<Language>(readStoredLanguage);

  const value = useMemo(
    () => ({
      language,
      setLanguage: (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem("pg_lang", lang);
      },
      t: (key: MessageKey, vars?: Record<string, string | number>) =>
        interpolate(messages[language][key], vars),
    }),
    [language],
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useI18n must be used within LanguageProvider");
  return ctx;
}

/** Luôn dùng tiếng Việt cho /admin và /admin/login (không phụ thuộc ngôn ngữ trang chơi). */
export function adminT(key: MessageKey, vars?: Record<string, string | number>): string {
  return interpolate(messages.vi[key], vars);
}

export function cycleLanguage(current: Language): Language {
  const i = LANGS.indexOf(current);
  return LANGS[(i + 1) % LANGS.length]!;
}
